export type ThemeGroup = 'Classic' | 'Game Systems' | 'Operating Systems'

export interface AppTheme {
  id: ThemeId
  name: string
  group: ThemeGroup
  css: ThemeCssVars
}

function theme(
  id: ThemeId,
  name: string,
  c: Omit<ThemeCssVars, never>,
  group: ThemeGroup = 'Classic'
): AppTheme {
  return { id, name, group, css: c }
}

