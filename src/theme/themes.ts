/**
 * Default mesh / material paint color for a theme.
 * Never returns black or near-black — always a lighter, theme-fitting tint.
 */
export function getThemeMaterialHex(themeId: ThemeId): string {
  const override = MATERIAL_DEFAULT_HEX[themeId]
  if (override && relativeLuminance(override) >= MIN_MATERIAL_LUMINANCE) {
    return override
  }

  const css = getTheme(themeId).css
  const candidates = [
    css['--mesh-hover'],
    css['--accent-orange'],
    css['--accent-green'],
    css['--accent-pink'],
    css['--mesh-selected'],
    css['--mesh-outline'],
    css['--accent'],
  ]

  for (const hex of candidates) {
    if (relativeLuminance(hex) >= MIN_MATERIAL_LUMINANCE) return hex
  }

  let hex = css['--accent'] || '#8ec8e8'
  for (let i = 0; i < 10 && relativeLuminance(hex) < MIN_MATERIAL_LUMINANCE; i++) {
    hex = lightenHex(hex, 0.22)
  }
  return relativeLuminance(hex) >= MIN_MATERIAL_LUMINANCE ? hex : '#8ec8e8'
}

export function getThemeMaterialColor(themeId: ThemeId): number {
  return hexToNumber(getThemeMaterialHex(themeId))
}
