export const DEFAULT_THEME_ID: ThemeId = 'nes'

const themeById = new Map(THEMES.map((t) => [t.id, t]))

export function getTheme(id: ThemeId): AppTheme {
  return themeById.get(id) ?? themeById.get(DEFAULT_THEME_ID)!
}

export function isThemeId(value: string): value is ThemeId {
  return themeById.has(value as ThemeId)
}

const DEEP_VIEWPORT_MODES = new Set([
  'flat',
  'smooth',
  'wireframe',
  'solid-wire',
  'unlit',
])

export function getViewportBackground(
  themeId: ThemeId,
  displayMode: string
): string {
  const t = getTheme(themeId).css
  return DEEP_VIEWPORT_MODES.has(displayMode)
    ? t['--viewport-bg-deep']
    : t['--viewport-bg']
}

export function hexToNumber(hex: string): number {
  const h = hex.startsWith('#') ? hex.slice(1) : hex
  return parseInt(h, 16)
}
